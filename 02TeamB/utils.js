var M_PI = 3.14159;

/****************************************************************************
 * deg2rad()
 ***************************************************************************/
function deg2rad(value) {
    return ((M_PI / 180.0) * (value));
}

/****************************************************************************
 * rad2deg()
 ***************************************************************************/
function rad2deg(value) {
    return ((value) * (180.0 / M_PI));
}
